//Подсчет суммы
const startDateInput = document.getElementById('date_begin');
const endDateInput = document.getElementById('date_end');
const resultElement = document.querySelector(".final_sum");
const default_sum = parseFloat(document.querySelector(".rate_sum_for_1_day").textContent);
const num_of_days = document.querySelector("#num_of_days");
var datePicker_begin;
var datePicker_end;

function calculateDateDifference() {
    const startDateValue = new Date(startDateInput.value);
    const endDateValue = new Date(endDateInput.value);

    const differenceInMilliseconds = endDateValue - startDateValue;
    const differenceInDays = differenceInMilliseconds / (1000 * 60 * 60 * 24);

    
    datePicker_begin = startDateInput.value;
    datePicker_end = endDateInput.value;

    if (isNaN(differenceInDays)) {
        resultElement.textContent = '0';
    }
    else if (differenceInDays < 7) {
        resultElement.textContent = parseFloat(differenceInDays * default_sum);
    }
    else if (differenceInDays >= 7 && differenceInDays < 30) {
        resultElement.textContent = parseFloat(differenceInDays * default_sum * 0.9).toFixed(1);
    }
    else if (differenceInDays >= 30) {
        resultElement.textContent = parseFloat(differenceInDays * default_sum * 0.8).toFixed(1);
    }
    if (differenceInDays > 0) {
        num_of_days.textContent = differenceInDays;
    }
}

startDateInput.addEventListener('input', calculateDateDifference);
endDateInput.addEventListener('input', calculateDateDifference);


